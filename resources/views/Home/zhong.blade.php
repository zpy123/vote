   <h3>帮助中心 </h3>
    <b>1.注册中国酒店设计网的会员的好处?</b>

    <p>a)免费在中国酒店设计网发布、查询各种分类信息；</p>
    <p>b)免费在中国酒店设计社区发帖、回帖；</p>
    <p>c)拥有会员中心，可以直接管理您所发布的所有信息，查询其他用户给您的回复，而且不会丢失任何的信息；</p>
    <p>d)免费参与网站活动。</p>

    <b>2.如何注册成为中国酒店设计会员?</b>

    <p>第一步：点击网站顶部的注册，进入注册页面</p>

    <p>第二步：填写注册信息后提交</p>

    <p>第三步：注册成功</p>

    <p>提示：建议您使用Gmail，新浪，搜狐，21Cn等邮箱系统，某些邮箱可能会将本站邮件误置为垃圾邮件，请在垃圾邮件类查询。如您没有收到系统邮件，请您第二天查收。</p>

    <b>3.注册后可以更改用户名吗？</b>

    <p>答：用户注册时候的邮箱和用户名在注册完成后是无法修改的，所以用户在注册的时候要仔细填写。</p>

    <b>4.提交注册信息后，如何验证我的邮箱？</b>

    <p>答：在您提交完注册信息后,系统会给您注册时填写的邮箱里发送一封验证邮件，邮件标题应为“注册激活”，点击此邮件中的链接，即可验证您的帐号！</p>

    <b>5.忘记密码或密码丢失了怎么办？</b>

    <p>答：您可以点击登录页面的“忘记密码”，通过注册时填写的邮箱来取回您的密码。您也可以联系我们的客服人员为您提供帮助。</p>

    <b>6.注册后我在哪里修改我的用户资料？</b>

    <p>答：您可以在任意页面的顶部登录，登录后点击 “会员中心”进入会员中心，就可以对您的用户资料进行修改了</p>

    <b>7.我的个人资料安全吗？</b>

    <p>答：除了您发布信息时所填写的，希望其他用户看到的个人资料以外，中国酒店设计网将对您其他的个人资料，如您的电话号码，邮箱等，采取严格的保密措施</p>

    <b>8.怎样输入验证码？</b>

    <p>答：4位认证数字，每个数字都可能是0-9数字中的任何一个。</p>



