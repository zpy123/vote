        美假微酒店投资有限公司（英文商标：GRIDINNTM）创立于2011年10月，是中国首家微酒店品牌，酒店以“微”为核心，为消费者提供“空间迷你，简单时尚，低碳环保，无线覆盖”的住宿环境，并以88-138元的市场定价服务客户，致力为追求简单，自由，快乐的人们提供“无微不至”的生活空间。
虽为连锁酒店的后起之秀，但精准的定位令其与市场上的星级酒店，商务酒店，便捷酒店形成差异化，广受大众尤其是年轻人的偏爱。截至至2014年下半年，格子微酒店连锁已在南宁核心地段开设了20家连锁分店。分别是南宁青山店、南宁解放店、南宁江南店、南宁中山店、南宁秀灵店、南宁西大店、南宁琅东店、葫芦鼎店、大沙田店、明秀店、仙葫羽笛店、五里亭店、南宁思贤店、南宁东葛店、南宁宾阳店、南宁楠梧店等，另外在南宁之外的城市也已陆续开设分店，分别是梧州中山店、玉林青年广场店、桂林店......
